// ===== BASIC GETTER (RAW OBJECT) =====
export const getWhereToLiveRegionBySlug = (slug) =>
  whereToLiveRegionDetails[slug] || null;

// ===== SMALL HELPER TO PICK LANGUAGE =====
const pickLang = (value, lang) => {
  if (!value) return value;

  if (typeof value === "string") return value; // already single-language

  if (typeof value === "object") {
    if (value[lang]) return value[lang];
    if (value.en) return value.en;
  }

  return value;
};

